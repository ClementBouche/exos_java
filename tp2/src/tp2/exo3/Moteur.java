package tp2.exo3;


public class Moteur{
	
	private int nbChevaux;
	
	// Constructor
	public Moteur(int nbChev) {
		this.nbChevaux = nbChev;
	}
	
	// Setters and Getters
	public void setNbChevaux(int nbChev) {
		this.nbChevaux = nbChev;
		}
	
	public int getNbChevaux() {
		return this.nbChevaux;
	}

}